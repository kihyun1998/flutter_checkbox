/// Controls which side of the label the checkbox appears on in a
/// [FlutterCheckboxTile].
enum CheckboxPosition {
  /// Checkbox appears before the label (leading edge).
  ///
  /// In LTR layouts this is the left side; in RTL it is the right side.
  start,

  /// Checkbox appears after the label (trailing edge).
  ///
  /// In LTR layouts this is the right side; in RTL it is the left side.
  end,
}
